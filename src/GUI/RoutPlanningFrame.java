    public void showChosenDistricts(HashSet<Integer> chosenLots) {
        districtsListModel.clear();
        ArrayList<String> dl;
        for (Integer key : lots.keySet()) {
            if (chosenLots.contains(key)) {
                dl = lots.get(key);
                for (String district : dl) {
                    districtsListModel.addElement(district);
                }
            }
        }
    }

